const minimumAge = 18;
let currentAge = 17 ;
if (currentAge >= 18) {
    console.log('You are eligible for Driving license ');
} 
else if (currentAge >= 16) {
    console.log("You're eligible for Learning Driving license")
}
else {
    const timeYouNeed = minimumAge-currentAge;
    console.log(`You are not eligible for Driving license, you are ${timeYouNeed} younger then the minimum age`) }
